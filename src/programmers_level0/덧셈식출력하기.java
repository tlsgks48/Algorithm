package programmers_level0;

import java.util.Scanner;

public class 덧셈식출력하기 {
    public class Solution {
        public static void main(String[] args) {
            Scanner sc = new Scanner(System.in);
            int a = sc.nextInt();
            int b = sc.nextInt();

            System.out.println(a + " + " + b + " = " + (a + b));
        }
    }
}
